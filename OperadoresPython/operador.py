a = 3
b = 2 
# Operador suma
suma = a+b
print("Suma: ",suma)

# Operador resta
resta = a-b
print("Resta: ",resta)


# Operador multiplicacion
mul = a*b
print("Multiplicacion: ",mul)


# Operador division
div = a/b
print("Division: ",div)

# Operador modulo
mod = a%b
print("Modulo: ",mod)

# Operador exponente
exp = a**b
print("Exponente: ",exp)





